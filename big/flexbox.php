<h2>Flexbox Research</h2>
    <p>Ways of building sites continuously improve and it’s important to keep up with the changes to keep your sites dynamic and responsive. Use of flexbox is one of the ways to improve site building. The default behavior of HTML elements are to stack so to change their behavior CSS elements are necessary to use but there are still limitations (Toptal Engineering Blog). There are scenarios where rather than stacking you may want to align elements differently to improve the look of a website. For example, using float is an option but this property limits one to arranging them horizontally and at some point the wrapper must be cleared to avoid overflowing what comes after (Toptal Engineering Blog). Developers have come up with a more dynamic way of laying out elements.</p>

    <p>“The Flexible Box Layout Module, makes it easier to design flexible responsive layout structure without using float or positioning” (W3Schools). One example of an ideal use for flexbox is to display navigation items horizontally but use flexbox properties to allow equally distributed space around the items (MDN Web Docs). Another good use of flexbox is to allow laying out of containers with different sized content in a way that avoids the problem of a container with less content rising to the bottom of the content (MDN Web Docs).</p>

    <p>Although the benefits to using flexbox are fairly clear there can be some challenges with browser support. Most of the material available on flexbox gives positive reviews of browser support with most sites. There are a couple of browsers that should be kept in mind for cross-browser availability that require a prefix when using flexbox which are Internet Explorer 10 and UC Browser (MDN Web Docs). </p>
