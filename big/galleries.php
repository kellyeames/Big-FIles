<h2>Galleries Research</h2>
     <p>In the past websites used to only display images as a flat JPG, GIF or PNG but over time developers created ways to display images in groups by using a slider plugin (Web Design Envato Tuts). Grouping images together allows for more space for other content on a website. The number of sliders that are available for use grows increasingly over time. A simple web search for possible plugins yields hundreds of options a user could incorporate into their website development.</p>

    <p>Sites that discuss jQuery plugins primarily consist of listing options and providing feedback on what each plugin provides in benefits and also provide helpful tips to other developers. Many options for plugins are free however some give an option to go premium for a fee. This option depends on the end goals because if a site is for a personal project it’s advisable to go with a free option but if the site is for a professional paid project it could be better to go with a premium option that will be more impressive in the end (1st Web Designer, 2019). </p>

    <p>One example of a free jQuery option is called Camera Slideshow which allow easy implementation of html objects, videos and captions (1st Web Designer, 2019). Another example is Galleria which is a javascript gallery built in a way that simplifies the process of creating an image gallery for users (1st Web Designer, 2019). </p>
    
